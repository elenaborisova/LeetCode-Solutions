def peak_index_mountain_array(arr):
    return arr.index(max(arr))


print(peak_index_mountain_array([0, 1, 0]))
print(peak_index_mountain_array([0, 2, 1, 0]))
print(peak_index_mountain_array([0, 10, 5, 2]))
print(peak_index_mountain_array([3, 4, 5, 1]))
print(peak_index_mountain_array([24, 69, 100, 99, 79, 78, 67, 36, 26, 19]))