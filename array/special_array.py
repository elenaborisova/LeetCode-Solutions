def special_array(nums):
    for num in range(0, max(nums) + 1):
        count = len([x for x in nums if x >= num])
        if num == count:
            return num

    return -1


print(special_array([3, 5]))
print(special_array([0, 0]))
print(special_array([0, 4, 3, 0, 4]))
